  setLEDs(ledColorValues);
  HAL_TIM_PWM_Start_DMA(&htim17, TIM_CHANNEL_1, (uint32_t*)ledPWMPeriods, sizeof(ledPWMPeriods) / sizeof(ledPWMPeriods[0]));

