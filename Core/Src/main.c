void setLEDs(uint8_t grbValues[]) {
  for (int i = 0; i < 6; i++) {
    uint8_t colorValue = grbValues[i];

    for (int k = 0; k < 8; k++) {
      int index = 8 * i + k;
      int bitMask = 1 << (7 - k);
      int bitValue = colorValue & bitMask;

      ledPWMPeriods[index] = bitValue ? 100 : 50;
    }
  }
}

